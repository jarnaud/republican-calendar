package com.github.jarnaud.republican;

/**
 * Republican special days (Sansculottide)
 * <p>
 * 5 days at the end of the year, 6 days every 4 years.
 */
public enum RSpecialDay {
    Virtue, // 17 sept.
    Genius, // 18 sept.
    Work, // 19 sept.
    Opinion, // 20 sept.
    Recompense, // 21 sept.
    Revolution // 22 sept. bis, occurs once every 4 years.
}
